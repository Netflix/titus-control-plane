package com.netflix.titus.master.endpoint.v2.rest;

import javax.ws.rs.GET;
import javax.ws.rs.Path;
import javax.ws.rs.Produces;
import javax.ws.rs.core.MediaType;
import javax.ws.rs.core.Response;

/**
 * Internal REST API for exposing resource consumption information.
 */
@Path(ResourceConsumptionEndpoint.PATH_API_V2_RESOURCE_CONSUMPTION)
@Produces(MediaType.APPLICATION_JSON)
public interface ResourceConsumptionEndpoint {
    String PATH_API_V2_RESOURCE_CONSUMPTION = "/api/v2/resource/consumption";

    @GET
    Response getSystemConsumption();
}
